import static org.junit.jupiter.api.Assertions.assertEquals;

import org.junit.jupiter.api.Test;

public class SummationTest {

	@Test
	public void sumOneThroughTen() {
		assertEquals(55, Summation.sumNumbers(1, 10));
	}

	@Test
	public void sumOneThroughOneHundred() {
		assertEquals(5050, Summation.sumNumbers(1, 100));
	}

	@Test
	public void sumNegativeNumbers() {
		assertEquals(-55, Summation.sumNumbers(-1, -10));
	}

	@Test
	public void sumStartNeg() {
		assertEquals(15, Summation.sumNumbers(-3, 6));
	}

	@Test
	public void sumEndNeg() {
		assertEquals(-15, Summation.sumNumbers(3, -6));
	}

}
